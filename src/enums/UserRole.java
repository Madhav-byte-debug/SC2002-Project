package enums;

/**
 * The UserRole enum represents the different roles a user can have
 * in the hospital management system.
 */
public enum UserRole {
    /**
     * Represents a patient user.
     */
    PATIENT,

    /**
     * Represents a doctor user.
     */
    DOCTOR,

    /**
     * Represents a pharmacist user.
     */
    PHARMACIST,

    /**
     * Represents an administrator user.
     */
    ADMINISTRATOR
}
