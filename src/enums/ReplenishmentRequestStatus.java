package enums;

/**
 * The ReplenishmentRequestStatus enum represents the status of a replenishment request
 * in the hospital management system.
 */
public enum ReplenishmentRequestStatus {
    /**
     * Indicates that the replenishment request has been approved.
     */
    APPROVED,

    /**
     * Indicates that the replenishment request has been declined.
     */
    DECLINED
}
