package enums;

/**
 * The DoctorAvailabilityStatus enum represents the availability status of a doctor
 * for appointments in the hospital management system.
 */
public enum DoctorAvailabilityStatus {
    /**
     * Indicates that the doctor is booked and unavailable for appointments.
     */
    BOOKED,

    /**
     * Indicates that the doctor is available for appointments.
     */
    AVAILABLE
}
